class Pais {
    let nombre: String
    let poblacion: Int
    let bandera: String
    
    init (
        nombre: String,
        poblacion: Int,
        bandera: String
    ) {
        self.nombre = nombre
        self.poblacion = poblacion
        self.bandera = bandera
    }
}
